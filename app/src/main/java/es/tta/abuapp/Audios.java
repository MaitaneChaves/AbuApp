package es.tta.abuapp;


public class Audios
{
    private String titulo;
    private String audio;
    private int nextAudio;

    public String getAudio() {
        return audio;
    }

    public void setAudio(String audio) {
        this.audio = audio;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public int getNextAudio() {
        return nextAudio;
    }

    public void setNextAudio(int nextAudio) {
        this.nextAudio = nextAudio;
    }
}
